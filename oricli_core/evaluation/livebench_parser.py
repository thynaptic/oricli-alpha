from __future__ import annotations
import json
import re
from pathlib import Path
from typing import Dict, Any, List
from collections import defaultdict


class LiveBenchResultParser:
    """
    Parses LiveBench benchmark results and provides summarized reporting
    and knowledge gap analysis.
    """

    # Recommendations mapping based on curriculum and target datasets
    RECOMMENDATIONS = {
        "reasoning": ("Stage 4: Capability", ["kitsdk/hotpot_qa"]),
        "coding": (
            "Stage 7: Coding",
            [
                "iamtarun/python_code_instructions_18k_alpaca",
                "m-a-p/CodeFeedback-Filtered-Instruction",
            ],
        ),
        "math": ("Stage 2: Logic", ["microsoft/orca-math-word-problems-200k"]),
        "knowledge": ("Stage 6: Knowledge", ["kitsdk/wiki_hop"]),
        "language": ("Stage 3: Prose", ["mlfoundations-dev/oh-dcft-v3.1-gemini-1.5-pro"]),
        "instruction_following": ("Stage 8: Alignment", ["Intel/orca_dpo_pairs"]),
        "data_analysis": ("Stage 5: Integration", ["oricli/data-analysis-curriculum"]),
    }

    def __init__(self, verbose: bool = False):
        self.verbose = verbose

    def clean_llm_output(self, text: str) -> str:
        """
        Cleans LLM output by removing reasoning tags, markdown code blocks,
        and extracting potential JSON content.
        """
        if not text:
            return ""

        # 1. Remove thinking tags
        text = re.sub(r"<think>.*?</think>", "", text, flags=re.DOTALL)

        # 2. Extract content from markdown code blocks if present
        json_match = re.search(r"```(?:json)?\s*(.*?)\s*```", text, flags=re.DOTALL)
        if json_match:
            text = json_match.group(1)
        
        # 3. If no code blocks, try to find the first '{' and last '}'
        else:
            start = text.find('{')
            end = text.rfind('}')
            if start != -1 and end != -1 and end > start:
                text = text[start:end+1]

        return text.strip()

    def parse_results(self, data_dir: Path) -> Dict[str, Any]:
        """
        Walks through the LiveBench data directory and parses ground_truth_judgment.jsonl files.
        """
        summary = {
            "categories": defaultdict(lambda: {"passed": 0, "total": 0}),
            "tasks": defaultdict(lambda: {"passed": 0, "total": 0}),
            "overall": {"passed": 0, "total": 0},
        }

        if not data_dir.exists():
            if self.verbose:
                print(f"Warning: Results directory {data_dir} does not exist.")
            return summary

        # Find all ground_truth_judgment.jsonl files
        judgment_files = list(data_dir.glob("**/model_judgment/ground_truth_judgment.jsonl"))

        for f in judgment_files:
            try:
                with open(f, "r") as file:
                    for line in file:
                        if not line.strip():
                            continue
                        
                        # Note: LiveBench results are usually clean JSONL, but we clean just in case
                        # though ground_truth_judgment.jsonl is generated by LiveBench logic, not LLM directly.
                        # The actual LLM output is in model_answer/*.jsonl
                        try:
                            data = json.loads(line)
                        except json.JSONDecodeError:
                            cleaned = self.clean_llm_output(line)
                            data = json.loads(cleaned)

                        score = data.get("score", 0)
                        category = data.get("category", "unknown")
                        task = data.get("task", "unknown")

                        # Update statistics
                        summary["overall"]["total"] += 1
                        summary["categories"][category]["total"] += 1
                        summary["tasks"][task]["total"] += 1

                        if score == 1:
                            summary["overall"]["passed"] += 1
                            summary["categories"][category]["passed"] += 1
                            summary["tasks"][task]["passed"] += 1
            except Exception as e:
                if self.verbose:
                    print(f"Error parsing {f}: {e}")

        return summary

    def get_knowledge_gaps(
        self, summary: Dict[str, Any], threshold: float = 0.5
    ) -> List[Dict[str, Any]]:
        """
        Identifies knowledge gaps based on category performance.
        """
        gaps = []
        for category, stats in summary["categories"].items():
            rate = stats["passed"] / stats["total"] if stats["total"] > 0 else 0
            if rate < threshold:
                rec = self.RECOMMENDATIONS.get(category, ("Unknown Stage", ["General Datasets"]))
                gaps.append(
                    {"category": category, "rate": rate, "stage": rec[0], "datasets": rec[1]}
                )
        return sorted(gaps, key=lambda x: x["rate"])

    def print_summary(self, summary: Dict[str, Any]):
        """
        Prints a formatted summary to the terminal.
        """
        print("\n" + "=" * 70)
        print("📊 MAVAIA COGNITIVE PERFORMANCE SUMMARY")
        print("=" * 70)

        # Categories Header
        print(f"{'Category':<25} {'Pass Rate':>10} {'Status':>10} {'Tests':>10}")
        print("-" * 70)

        sorted_categories = sorted(summary["categories"].items())
        for category, stats in sorted_categories:
            rate = stats["passed"] / stats["total"] if stats["total"] > 0 else 0
            status = "[PASS]" if rate >= 0.5 else "[GAP]"
            print(f"{category.capitalize():<25} {rate:>9.1%} {status:>10} {stats['total']:>10}")

        # Overall
        overall = summary["overall"]
        overall_rate = overall["passed"] / overall["total"] if overall["total"] > 0 else 0
        print("-" * 70)
        print(f"{'OVERALL':<25} {overall_rate:>9.1%} {'':>10} {overall['total']:>10}")

        # Knowledge Gaps
        gaps = self.get_knowledge_gaps(summary)
        if gaps:
            print("\n💡 RECOMMENDED RETOUCH STAGES:")
            for gap in gaps:
                print(f"  • {gap['category'].capitalize()}: {gap['stage']}")
                print(f"    Datasets: {', '.join(gap['datasets'])}")
        else:
            print("\n✨ No critical gaps detected! Foundation is solid.")

        print("=" * 70 + "\n")

    @classmethod
    def parse_all(cls, data_dir: Path, verbose: bool = False):
        """Helper to parse and print in one go."""
        parser = cls(verbose=verbose)
        summary = parser.parse_results(data_dir)
        if summary["overall"]["total"] > 0:
            parser.print_summary(summary)
        else:
            print("\n⚠ No benchmark results found to display.")
